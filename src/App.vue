    endIndex() {
      return this.page * this.counterTickersOnPage
    },
    filteredTickers() {
      return this.tickers.filter(t => t.name.toLowerCase().includes(this.filter.toLowerCase()))

    },

    paginatedTickers() {
      return this.filteredTickers.slice(this.startIndex, this.endIndex)
    },

